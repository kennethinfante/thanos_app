CREATE TABLE IF NOT EXISTS users (
id INTEGER PRIMARY KEY,
first_name TEXT,
middle_name TEXT,
last_name TEXT,
phone_number TEXT NOT NULL UNIQUE,
address TEXT,
gender TEXT,
email TEXT,
username TEXT,
password TEXT,
created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS account_types (
id INTEGER PRIMARY KEY,
name TEXT,
description TEXT
);

CREATE TABLE IF NOT EXISTS accounts (
id INTEGER PRIMARY KEY,
code TEXT NOT NULL UNIQUE,
name TEXT,
description TEXT,
account_type_id INTEGER,
parent_account_id INTEGER,
is_active BOOLEAN DEFAULT 1,
created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
FOREIGN KEY (account_type_id) REFERENCES account_types(id),
FOREIGN KEY (parent_account_id) REFERENCES accounts(id)
);

CREATE TABLE IF NOT EXISTS customers (
id INTEGER PRIMARY KEY,
name TEXT,
contact_person TEXT,
phone TEXT,
email TEXT,
website TEXT,
tax_number TEXT NOT NULL UNIQUE,
address TEXT,
city TEXT,
state TEXT,
postal_code TEXT,
country TEXT,
description TEXT,
is_active BOOLEAN DEFAULT 1,
created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS vendors (
id INTEGER PRIMARY KEY,
name TEXT,
contact_person TEXT,
phone TEXT,
email TEXT,
website TEXT,
tax_number TEXT NOT NULL UNIQUE,
address TEXT,
city TEXT,
state TEXT,
postal_code TEXT,
country TEXT,
description TEXT,
is_active BOOLEAN DEFAULT 1,
created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS employees (
id INTEGER PRIMARY KEY,
first_name TEXT,
last_name TEXT,
name TEXT,
gender TEXT,
phone TEXT,
email TEXT,
website TEXT,
tax_number TEXT NOT NULL UNIQUE,
role TEXT,
address TEXT,
city TEXT,
state TEXT,
postal_code TEXT,
country TEXT,
username TEXT,
password TEXT,
is_active BOOLEAN DEFAULT 1,
created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS fiscal_years (
id INTEGER PRIMARY KEY,
name TEXT,
start_date TEXT,
end_date TEXT,
is_closed BOOLEAN DEFAULT 0,
created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS currencies (
id INTEGER PRIMARY KEY,
code TEXT NOT NULL UNIQUE,
name TEXT,
symbol TEXT,
is_default BOOLEAN DEFAULT 0,
created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS tax_rates (
id INTEGER PRIMARY KEY,
name TEXT,
rate TEXT,
description TEXT,
is_active BOOLEAN DEFAULT 1,
created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS payment_methods (
id INTEGER PRIMARY KEY,
name TEXT,
is_active BOOLEAN DEFAULT 1,
created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS bank_accounts (
id INTEGER PRIMARY KEY,
account_id INTEGER,
bank_name TEXT,
account_number TEXT NOT NULL UNIQUE,
description TEXT,
currency_id INTEGER,
opening_balance DECIMAL(15,2) DEFAULT 0,
current_balance DECIMAL(15,2) DEFAULT 0,
is_active BOOLEAN DEFAULT 1,
created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
FOREIGN KEY (account_id) REFERENCES accounts(id),
FOREIGN KEY (currency_id) REFERENCES currencies(id)
);

CREATE TABLE IF NOT EXISTS journals (
id INTEGER PRIMARY KEY,
date TEXT,
description TEXT,
is_posted BOOLEAN DEFAULT 0,
created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS journal_lines (
id INTEGER PRIMARY KEY,
journal_id INTEGER,
account_id INTEGER,
description TEXT,
debit DECIMAL(15,2) DEFAULT 0,
credit DECIMAL(15,2) DEFAULT 0,
FOREIGN KEY (journal_id) REFERENCES journals(id),
FOREIGN KEY (account_id) REFERENCES accounts(id)
);

CREATE TABLE IF NOT EXISTS items (
id INTEGER PRIMARY KEY,
name TEXT,
description TEXT,
sale_price DECIMAL(15,2) DEFAULT 0,
purchase_price DECIMAL(15,2) DEFAULT 0,
tax_rate_id INTEGER,
is_active BOOLEAN DEFAULT 1,
inventory_tracking TEXT,
is_consumable TEXT,
is_service TEXT,
current_stock TEXT,
reorder_level TEXT,
inventory_account_id INTEGER,
revenue_account_id INTEGER,
expense_account_id INTEGER,
created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
FOREIGN KEY (tax_rate_id) REFERENCES tax_rates(id),
FOREIGN KEY (inventory_account_id) REFERENCES accounts(id),
FOREIGN KEY (revenue_account_id) REFERENCES accounts(id),
FOREIGN KEY (expense_account_id) REFERENCES accounts(id)
);

CREATE TABLE IF NOT EXISTS invoices (
id INTEGER PRIMARY KEY,
customer_id INTEGER,
invoice_number TEXT NOT NULL UNIQUE,
date TEXT,
due_date TEXT,
subtotal TEXT,
tax_amount DECIMAL(15,2) DEFAULT 0,
total_amount DECIMAL(15,2) DEFAULT 0,
description TEXT,
status TEXT DEFAULT 'draft', -- draft, sent, paid, partially_paid, overdue, cancelled,
created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
FOREIGN KEY (customer_id) REFERENCES customers(id)
);

CREATE TABLE IF NOT EXISTS invoice_lines (
id INTEGER PRIMARY KEY,
invoice_id INTEGER,
item_id INTEGER,
account_id INTEGER,
description TEXT,
quantity TEXT,
unit_price DECIMAL(15,2) DEFAULT 0,
tax_rate_id INTEGER,
subtotal TEXT,
tax_amount DECIMAL(15,2) DEFAULT 0,
line_amount DECIMAL(15,2) DEFAULT 0,
FOREIGN KEY (invoice_id) REFERENCES invoices(id),
FOREIGN KEY (item_id) REFERENCES items(id),
FOREIGN KEY (tax_rate_id) REFERENCES tax_rates(id),
FOREIGN KEY (account_id) REFERENCES accounts(id)
);

CREATE TABLE IF NOT EXISTS invoice_payments (
id INTEGER PRIMARY KEY,
invoice_id INTEGER,
date TEXT,
amount DECIMAL(15,2) DEFAULT 0,
description TEXT,
payment_method_id INTEGER,
account_id INTEGER,
created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
FOREIGN KEY (invoice_id) REFERENCES invoices(id),
FOREIGN KEY (payment_method_id) REFERENCES payment_methods(id),
FOREIGN KEY (account_id) REFERENCES accounts(id)
);

CREATE TABLE IF NOT EXISTS bills (
id INTEGER PRIMARY KEY,
vendor_id INTEGER,
bill_number TEXT NOT NULL UNIQUE,
date TEXT,
due_date TEXT,
subtotal TEXT,
tax_amount DECIMAL(15,2) DEFAULT 0,
total_amount DECIMAL(15,2) DEFAULT 0,
description TEXT,
status TEXT DEFAULT 'draft', -- draft, received, paid, partially_paid, overdue, cancelled,
created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
FOREIGN KEY (vendor_id) REFERENCES vendors(id)
);

CREATE TABLE IF NOT EXISTS bill_lines (
id INTEGER PRIMARY KEY,
bill_id INTEGER,
item_id INTEGER,
account_id INTEGER,
description TEXT,
quantity TEXT,
unit_price DECIMAL(15,2) DEFAULT 0,
tax_rate_id INTEGER,
subtotal TEXT,
tax_amount DECIMAL(15,2) DEFAULT 0,
line_amount DECIMAL(15,2) DEFAULT 0,
FOREIGN KEY (bill_id) REFERENCES bills(id),
FOREIGN KEY (item_id) REFERENCES items(id),
FOREIGN KEY (tax_rate_id) REFERENCES tax_rates(id),
FOREIGN KEY (account_id) REFERENCES accounts(id)
);

CREATE TABLE IF NOT EXISTS bill_payments (
id INTEGER PRIMARY KEY,
bill_id INTEGER,
date TEXT,
amount DECIMAL(15,2) DEFAULT 0,
description TEXT,
payment_method_id INTEGER,
account_id INTEGER,
created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
FOREIGN KEY (bill_id) REFERENCES bills(id),
FOREIGN KEY (payment_method_id) REFERENCES payment_methods(id),
FOREIGN KEY (account_id) REFERENCES accounts(id)
);

CREATE TABLE IF NOT EXISTS cash_transactions (
id INTEGER PRIMARY KEY,
customer_id INTEGER,
vendor_id INTEGER,
cash_type TEXT,
date TEXT,
tax_rate_id INTEGER,
subtotal TEXT,
tax_amount DECIMAL(15,2) DEFAULT 0,
total_amount DECIMAL(15,2) DEFAULT 0,
description TEXT,
payment_method_id INTEGER,
account_id INTEGER,
created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
FOREIGN KEY (customer_id) REFERENCES customers(id),
FOREIGN KEY (vendor_id) REFERENCES vendors(id),
FOREIGN KEY (tax_rate_id) REFERENCES tax_rates(id),
FOREIGN KEY (payment_method_id) REFERENCES payment_methods(id),
FOREIGN KEY (account_id) REFERENCES accounts(id)
);

CREATE TABLE IF NOT EXISTS cash_transaction_lines (
id INTEGER PRIMARY KEY,
cash_transaction_id INTEGER,
item_id INTEGER,
account_id INTEGER,
description TEXT,
quantity TEXT,
unit_price DECIMAL(15,2) DEFAULT 0,
tax_rate_id INTEGER,
subtotal TEXT,
